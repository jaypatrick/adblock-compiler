/**
 * Gets or creates a persistent dynamic Worker for the given authenticated user via env.LOADER.
 *
 * Returns `null` if `env.LOADER` is not configured (LOADER binding absent).
 *
 * @param userId - The authenticated user's Better Auth user ID.
 * @param request - The incoming HTTP request to dispatch into the agent Worker.
 * @param env - The Worker environment bindings.
 */
export async function getOrCreateUserAgent(
    userId: string,
    request: Request,
    env: Env,
): Promise<Response | null> {
    if (!isLoaderAvailable(env)) {
        return null;
    }

    const workerId = makeAgentWorkerId(userId);

    const agentBindings: DynamicWorkerSafeBindings = {};
    if (env.COMPILATION_CACHE) agentBindings.COMPILATION_CACHE = env.COMPILATION_CACHE;
    if (env.METRICS) agentBindings.METRICS = env.METRICS;

    try {
        const agentWorker = await env.LOADER!.get(
            workerId,
            (_id: string) => ({
                compatibilityDate: '2026-01-01',
                mainModule: 'agent-main.js',
                modules: {
                    // TODO(#1386): Replace with bundled agents SDK AiAgent source once @cloudflare/worker-bundler
                    'agent-main.js': getAgentWorkerSource(),
                },
                globalOutbound: null,
                bindings: agentBindings,
            }),
        );

        return agentWorker.fetch(request);
    } catch (error) {
        const message = error instanceof Error ? error.message : String(error);
        // deno-lint-ignore no-console
        console.error(`[DynamicWorkers] Failed to get/create user agent for ${userId}:`, message);
        return null;
    }
}
