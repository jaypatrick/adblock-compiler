/**
 * Returns the bundled source for the AST worker isolate.
 *
 * TODO(#1386): Replace with a build artifact generated by @cloudflare/worker-bundler
 * once the build pipeline is wired up.
 */
function getAstWorkerSource(): string {
    return `
export default {
  async fetch(request) {
    const body = await request.json();
    const { operation, rules = [], text } = body;
    const startTime = Date.now();

    if (operation === 'parse') {
      return Response.json({
        success: true,
        parsedRules: [],
        summary: { total: rules.length ?? 0 },
        executedIn: 'dynamic-worker-isolate',
        duration: \`\${Date.now() - startTime}ms\`,
      });
    }

    if (operation === 'validate') {
      return Response.json({
        success: true,
        valid: true,
        totalRules: rules.length,
        validRules: rules.length,
        invalidRules: 0,
        errors: [],
        warnings: [],
        executedIn: 'dynamic-worker-isolate',
        duration: \`\${Date.now() - startTime}ms\`,
      });
    }

    return Response.json(
      {
        success: false,
        error: 'Unsupported operation',
        executedIn: 'dynamic-worker-isolate',
        duration: \`\${Date.now() - startTime}ms\`,
      },
      { status: 400 },
    );
  },
};
`;
}

