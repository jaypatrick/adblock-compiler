/**
 * Runs AST parsing inside an ephemeral dynamic Worker isolate via env.LOADER.
 *
 * The isolate has NO network access (`globalOutbound: null`) and NO bindings —
 * it receives only the request body and returns parsed AST data.
 *
 * Falls back to `null` if `env.LOADER` is not configured, allowing the caller
 * to fall back to the existing in-process `handleASTParseRequest()`.
 */
export async function runAstParseInDynamicWorker(
    options: DynamicAstParseOptions,
    env: Env,
): Promise<DynamicWorkerResult | null> {
    if (!isLoaderAvailable(env)) {
        return null;
    }

    const startTime = Date.now();
    try {
        const worker = await env.LOADER!.load({
            compatibilityDate: '2026-01-01',
            mainModule: 'ast-worker.js',
            modules: {
                'ast-worker.js': getAstWorkerSource(),
            },
            globalOutbound: null,
            bindings: {},
        });

        const response = await worker.fetch(
            new Request('https://dynamic-worker/ast', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({ operation: 'parse', ...options }),
            }),
        );

        const durationMs = Date.now() - startTime;

        if (!response.ok) {
            const errorMessage = await parseIsolateErrorMessage(response);
            return { success: false, status: response.status, error: errorMessage, durationMs };
        }

        const data = await response.json();
        return { success: true, status: response.status, data, durationMs };
    } catch (error) {
        const message = error instanceof Error ? error.message : String(error);
        return { success: false, error: message, durationMs: Date.now() - startTime };
    }
}

