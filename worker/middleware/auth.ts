/**
 * Unified Authentication Middleware
 *
 * Authenticates requests via a four-tier chain:
 *   1. **API key** — Bearer token with `abc_` prefix, hashed & looked up in PostgreSQL
 *   2. **Better Auth (primary)** — Cookie or bearer-plugin session via the primary `authProvider`
 *   3. **Clerk JWT (deprecated fallback)** — Only attempted when a `fallbackProvider` is supplied
 *      and the primary provider found no session (no error). Set `DISABLE_CLERK_FALLBACK=true`
 *      to skip this step entirely.
 *   4. **Anonymous** — No credentials; falls through to anonymous context
 *
 * Token disambiguation:
 *   - API keys always start with `abc_` (generated by api-key-utils.ts)
 *   - Clerk JWTs are dot-separated base64 strings (header.payload.signature)
 *   - If a Bearer token matches neither pattern, it is rejected
 *
 * Legacy support:
 *   - `authenticateApiKey()` is preserved for backward compatibility but
 *     should be migrated to `authenticateRequestUnified()`.
 *
 * @see worker/middleware/clerk-jwt.ts — JWT verification
 * @see worker/types.ts — IAuthContext, IAuthMiddlewareResult, UserTier
 */

import type { Env, HyperdriveBinding, IAuthProvider } from '../types.ts';
import { ANONYMOUS_AUTH_CONTEXT, AuthScope, type IAuthContext, type IAuthMiddlewareResult, isTierSufficient, TIER_REGISTRY, UserTier } from '../types.ts';
import { ClerkAuthProvider } from './clerk-auth-provider.ts';
import { runTokenValidators } from './token-validator.ts';
import { ApiKeyRowSchema, UserTierRowSchema } from '../schemas.ts';
import { z } from 'zod';

